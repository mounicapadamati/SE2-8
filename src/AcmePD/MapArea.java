package AcmePD;

public class MapArea {
	private String name;
	private String blocksToMile;
	private float courierAvgSpeed;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBlocksToMile() {
		return blocksToMile;
	}
	public void setBlocksToMile(String blocksToMile) {
		this.blocksToMile = blocksToMile;
	}
	public float getCourierAvgSpeed() {
		return courierAvgSpeed;
	}
	public void setCourierAvgSpeed(float courierAvgSpeed) {
		this.courierAvgSpeed = courierAvgSpeed;
	}

}
